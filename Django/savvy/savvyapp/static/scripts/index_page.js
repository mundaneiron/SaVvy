function openSignUp() {
  var popup = document.getElementById('signUpPopup');
  if (popup.style.display === 'flex') {
      popup.style.display = 'none'; 
  } else {
      popup.style.display = 'flex'; 
  }
}
function closeSignUp() {
  document.getElementById('signUpPopup').style.display = 'none';
}

function openSignIn() {
  document.getElementById('signUpPopup').style.display = 'none';
}

